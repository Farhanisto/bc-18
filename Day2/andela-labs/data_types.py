def data_type(n):
  if isinstance(n,str):
     return len(n)
     
  if n is None:
    return 'no value'
    
  if isinstance(n,bool):
    return n
    
  if isinstance(n,int):
    if n<100:
      return 'less than 100'
    elif n>100:
      return 'more than 100'
    else:
      return 'equal to 100'
  if isinstance(n,list):
    if len(n)<3:
      return None
    else:
      return n[2]
